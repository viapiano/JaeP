public class Rechner
{
   public int addition (int a, int b) 
   {
      return a + b;
   }
   
   public int subtraktion (int a, int b)
   {
      return a - b;
   }
   
   public int multiplikation (int a, int b)
   {
      return a * b;
   }
   
   public float division (int a, int b) 
   {
      return a / b;
   }
}
